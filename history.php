						<h1>Ремесленное училище. Послевоенное восстановление</h1>
					</header>
					<p>Великая Отечественная война и приход немцев вновь нарушили деятельность училища. Вместе с заводом много учеников и мастеров были эвакуированы за Урал. Но сразу же, после освобождения города 20&nbsp;октября 1943&nbsp;года Постановлением Госкомитета Обороны Главного Управления Трудовых Резервов от 5&nbsp;ноября 1943&nbsp;года был дан приказ восстановить на базе завода&nbsp;№&nbsp;270 ремесленное училище&nbsp;№&nbsp;7 с установкой начала срока обучения 1&nbsp;февраля 1944&nbsp;года.</p>
					<p>Но уже до начала набора пошли ребята, и до 5&nbsp;декабря уже было организовано 5&nbsp;групп, которые своими силами в тяжелых условиях начали восстановительные работы. Занятия начались вовремя.</p>
