					<header>
						<h1>ФЗУ</h1>
					</header>
					<p>В 1921&nbsp;году на совещании заведующих учебными заведениями Луганска принято решение об организации ФЗУ при заводе им.&nbsp;Обороны Красного Луганска. Содержится оно целиком на средства завода. Так начинается 3-й&nbsp;период в истории колледжа.</p>
					<p>В ФЗУ учились 212&nbsp;детей работников завода. Органом самоуправления был старостат, который руководил жизнью училища. В училище было 2&nbsp;класса: общеобразовательный и специальный, а также мастерские&nbsp;&mdash; слесарная, токарная и столярная. Ученики работали в мастерских по 4&nbsp;часа ежедневно, все занятия проводились по заводскому гудку. В 1923&nbsp;году училище получило новое помещение по улице 11&nbsp;Линия, 7 (ныне ул.&nbsp;Шеремета, 7), материальная база училища значительно окрепла, что позволило увеличить контингент учащихся, а также расширить их профессиональную подготовку.</p>
