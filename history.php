		<section class="sec sec-history sec-1st">
			<div class="container">
				<p>При открытии школы, кроме денежной суммы выделенной казной, собирались многочисленные пожертвования из чиновных лиц  завода &mdash; по 5-10&nbsp;рублей с каждого, а также помощь от Славяносербского земства &mdash; 20&nbsp;рублей.</p>
				<p>Горно-заводская школа не имела своего специального помещения, а нанимала его на участке земли обывателя Гавриила Свиридовича Сидорова по улице Казанской, на углу переулка, который выходил на улицу Петербургскую, недалеко от завода.</p>
				<p>Некоторое время горно-заводская школа была единственным учебным заведением в Луганске. Об этом свидетельствует выписка из отчета о народонаселении и других статистических данных: в поселке Луганский завод и поселении Каменный Брод на 1&nbsp;января 1831&nbsp;года:</p>
				<p>
					семинарий&nbsp;&mdash; нет;<br>
					гимназий&nbsp;&mdash; нет;<br>
					пансионов&nbsp;&mdash; нет;<br>
					приходских училищ и школ&nbsp;&mdash; нет; <br>
					горно-заводская школа&nbsp;&mdash; 1, её капитал&nbsp;&mdash; 450&nbsp;рублей в год.
				</p>
				<p>Горно-заводская школа дала образование и рабочие квалификации многим будущим работникам, о чем свидетельствуют формулярные списки, которые часто встречаются в архивных документах. Сначала в школе было всего два класса: в первом учились совсем неграмотные (20&nbsp;учеников), во втором (тоже 20&nbsp;чел.) &mdash; ученики, которые умели читать и писать. Постепенно курс обучения был доведен до четырёх лет, а позже &mdash; до пяти. В рапорте-справке Главного артиллерийского управления о задачах школы отмечалось: &laquo;Школа должна подготовить мастеров и рабочих для Патронного завода (их будущими руководителями). Необходимо, чтобы учащиеся с малых лет привыкали к заводскому начальству и считали бы себя, как бы частью завода&raquo;.</p>
			</div>
		</section>

		<section class="sec sec-history sec-2nd">
			<div class="container">
				<img src="img/history/Evgraf_Kovalevskiy-1865.png" height="350px" style="float: right;" alt="Кабалевский Клавдий Егорович" title="Кабалевский Клавдий Егорович">
				<p>Помещение школы на этот раз арендовали по договору с вдовой обывателя Аггея Павловича Иванова Феклой Никитичной по улице Садовой (бывшая Кузнечная), дом&nbsp;59  за 650&nbsp;рублей в год.</p>
				<p>Учебный год начинался 25&nbsp;августа и продолжался до мая. После 1910&nbsp;года завод арендовал для школы более просторный дом купца второй гильдии Васнева Сергея Павловича по улице Белой&nbsp;&mdash; 900&nbsp;рублей в год. При школе постоянно работали ремесленные классы по слесарно-токарной, кузнечного и столярно-плотницкого дела, а также кружки самодеятельности: художественного слова, песни и танца.</p>
				<p>За обучение учеников тоже бралась плата: от 10 до 50&nbsp;копеек в месяц в зависимости от заработка родителей. Тратились эти деньги исключительно на нужды детей: завтраки, учебники, прогулки. В мае 1909&nbsp;года состоялся первый выпуск учащихся, окончивших 4&nbsp;класса. Удостоверение получили 14&nbsp;учеников из 20&nbsp;поступивших в 1906&nbsp;году во второй класс. Вот их имена: Журавлева&nbsp;Евдокия, Иванов&nbsp;Костя, Климкова&nbsp;Анна,  Копачов&nbsp;Иван, Краснобаева&nbsp;Пелагея,  Линникова&nbsp;Ульяна,  Могильников&nbsp;Борис, Никитинская&nbsp;Мария, Пшеничная&nbsp;Александра, Рубан&nbsp;Яков, Сушников&nbsp;Анатолий, Шалашный&nbsp;Дионисий, Шамрай&nbsp;Иван.</p>
				<p>В 1915&nbsp;году начальник завода генерал-майор Павцов Александр Александрович посылает прошение в Главное артиллерийского управления о переименовании заводской школы в училище, однако решение этого вопроса управления отложило до окончания войны. Школа патронного завода продолжала работать как после февральской революции, так и после октября 1917&nbsp;года.  Более того, она начала  (хотя с опозданием на 2&nbsp;месяца) 1918-1919&nbsp;учебный год в период немецкой оккупации Украины в 1918&nbsp;году. И только с приходом в Луганск деникинцев в мае 1919&nbsp;года школа прекратила свою работу на 2&nbsp;года.</p>
			</div>

		</section>

