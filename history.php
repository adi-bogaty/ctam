					<header>
						<h1>Первое здание заводской школы</h1>
					</header>
					<p>В начале 20&nbsp;века начальник патронного завода генерал-майор Кабалевский Клавдий Егорович посылает к главному артиллерийского управления план-чертеж школьного здания, его сметную стоимость и просит отпустить средства для его сооружения.</p>
					<p>Было определено и место для строения&nbsp;&mdash; во дворе того же обывателя Сидорова&nbsp;Т.&nbsp;С., у которого снималось помещения для бывшей школы. Однако война с Японией в 1905&nbsp;году не позволяла казне выделить необходимую сумму и оттягивала открытие школы. Лишь в приказе по заводу от 3&nbsp;февраля 1906&nbsp;года генерал Кабалевский писал: &laquo;Военный совет 22&nbsp;декабря 1905&nbsp;года позволил открыть при вверенном мне заводе с 1&nbsp;января этого года, в наемном помещении, школы для детей рабочих, с ассигнованиям на ее содержание 2880&nbsp;рублей&raquo;. Школа была открыта 11&nbsp;февраля 1906&nbsp;года, руководство ею было поручено полковнику Максименко&nbsp;Н.&nbsp;С. Это свидетельствует о том, что эта заводская школа была прародительницей сегодняшнего колледжа.</p>
