					<header>
						<h1>СПТУ. Новый корпус</h1>
					</header>
					<p>В 70-е&nbsp;годы было принято решение о строительстве нового учебного корпуса колледжа, который вступил в строй в 1979&nbsp;году. С этого же года колледж стал средним профессионально-техническим училищем и вместе с профессией выпускники получали среднее образование. В новом корпусе были оборудованы шесть современных производственных мастерских, 12 учебных кабинетов, столовая на 210&nbsp;мест, библиотека с читальным залом, книжный фонд составлял 45&nbsp;тыс. экземпляров книг, актовый зал на 300&nbsp;посадочных мест с широкоформатной киноустановкой.</p>
					<p>В 2003&nbsp;году СПТУ&nbsp;№&nbsp;7 было реорганизовано в Луганский профессиональный машиностроительный лицей, а 27&nbsp;июля 2011&nbsp;года приказом Министерства образования и науки Украины лицей был введен в состав Луганского национального университета имени Тараса&nbsp;Шевченко в качестве отдельного структурного подразделения и переименован в  Высшее профессиональное училище&nbsp;ЛНУ.</p>
