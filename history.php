					<header>
						<h1>Основание</h1>
					</header>
					<p>Восемнадцатого октября 1826&nbsp;года начальник Луганского горного округа и Литейного завода Август Августович Гэс де Кальве в своем письме Российскому горному департаменту, которому был подчинен завод, отчитывался о своих деяниях о текущих 4-х лет&nbsp;правления. Перечислив производственные дела и нововведения, он рапортовал о строительстве новой больницы, открытие заводского музея, библиотеки: &laquo;<span style="font-weight: bold;">Генваря&nbsp;23&nbsp;1823</span> учредил Горную школу&raquo;. Это первое профессиональное училище не только Донбасса но и всей Левобережной Украины!</p>
					<p>Как  рассказывают архивные документы в  школу принимались дети чиновных лиц, а также мастеров и рабочих завода. Главной задачей школы была подготовка квалифицированной рабочей смены.</p>
