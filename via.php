				<p>Одним из видов досуговой деятельности студентов Колледжа технологий автотранспорта и машиностроения  является реализация своих музыкальных способностей и талантов. Для этого в колледже организован вокально-инструментальный ансамбль.</p>
				<p>Коллектив ВИА под руководством Чередниченко Игоря Андреевича существует   с 1990 года, за это время ансамбль неоднократно участвовал в различных республиканских и международных конкурсах и фестивалях, в их числе: «Наследники Победы», «На крыльях творчества», «Золотой дударик», «Джерела надій», «Формула успеха», «Крылья надежды»,  «Червона Рута»  и многих других. Участие и организация нескольких рок-сейшенов стало заметной страницей в истории коллектива.</p>
				<p>Вокальная группа функционирует как в режиме вокалистов-солистов, так и вокальной части ВИА. Солисты выступают под фонограммы «минус», которые записываются руководителем ансамбля, в тоже время вокалисты поют и под «живое» сопровождение инструментальной группы. В силу сложившихся музыкальных функций, исполнение под аккомпанемент инструментальной группы, в основном, используется в произведениях, характеризующих рок-музыку, а под фонограмму «минус» – эстрадную и популярную. В репертуар ВИА входят песни таких групп как «Ария», «Кино», «ДДТ», «Metallica» и других. В репертуар солистов – песни  как отечественных эстрадных исполнителей так и  стран зарубежья. Иными словами развитие музыкального мастерства и мышления проходит на лучших образцах российской и мировой эстрадной музыки.</p>
				<p>В коллективе часто используются песни и самих участников коллектива, аранжировки делает руководитель ВИА, причем особое внимание уделяется и народной музыке. Ряд песен для трехголосного  пения в рок и бит-аранжировках пользуются особой  популярностью у студентов колледжа. К их числу относятся русские,  белорусские и украинские народные песни, что влияет на развитие многоголосного пения и воспитывает любовь к родному краю.В коллектив приглашаются студенты, которые имеют музыкальное образование или просто увлекающиеся игрой на инструментах и достигших определенного уровня, хорошие вокалисты любого уровня развития и просто желающие. Для каждого руководитель пишет партии, подходящие именно данному исполнителю, то есть существует индивидуальный подход к каждому музыканту. Обязательное изучение нотной грамоты (все произведения расписаны в нотах!) подвигает образовательный уровень и развивает навык «чтения с листа».</p>
				<p>В общем, каждый участник коллектива ВИА проходит хорошую музыкальную подготовку, как в исполнительском, так и в теоретическом плане. Каждый получает бесценный опыт концертных выступлений и окрыляется положительными эмоциями и переживаниями от своего участия в программах ансамбля, что позволяет добиваться высокого музыкального качества материала. Новой страницей в коллективе  стало техническое применение компьютерных достижений в музыке, освоение учащимися музыкальных программ для написания музыки на ПК и звуковое обеспечения мероприятий.</p>
				<p>Главной и основной задачей коллективов ВИА является участие в профориентационных мероприятиях и концертах. Неотъемлемым  заданием является привлечение молодежи к творчеству, удовлетворение своих запросов в общественной жизни и заполнение своего досуга полезными интересами, одним из которых являлась, является, и будет являться музыка.</p>
