				<h1>Руководитель ВИА</h1>
				<img src="img/via/via9.png" alt="Руководитель ВИА — Чередниченко&nbsp;И.&nbsp;А." title="Руководитель ВИА — Чередниченко&nbsp;И.&nbsp;А." class="person-img" width="200px">
				<p>Руководитель вокально-инструментального ансамбля <em>Чередниченко Игорь Андреевич</em> работает в Колледже технологий автотранспорта и машиностроения Луганского университета имени Тараса Шевченко  с 29 марта 1990 года. Проявил себя как замечательный организатор музыкальных коллективов и прекрасный специалист.</p>
				<p>Умеет: «расшифровать» музыку с носителя, записать ее в ноты и разучить с коллективом, аранжировать произведение, переложить для любого состава инструментов, сделать инструментовку и записать фонограмму «минус» для солистов, написать песню от мелодии до инструментовки.</p>
				<p>Автор многих песен и Гимна колледжа. Техническое обеспечение также под его началом. Имеет три образования: техническое, гуманитарное и педагогическое, а именно окончил политехнический колледж, колледж культуры и искусств, Луганский национальный университет имени Тараса Шевченко. Закончил магистратуру с присвоением квалификации «Магистр музыкальной педагогики и воспитания, преподаватель вокала».</p>
				<p>Считает, что в современных условиях на первый план выходит обучаемость и повышение квалификации специалиста на протяжении всей трудовой деятельности, в соответствии с научно-техническим прогрессом во всех профессиональных сферах.</p>
